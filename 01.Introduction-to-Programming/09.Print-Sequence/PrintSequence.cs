using System;

/// <summary>
/// Write a program that prints the first 10 members of the sequence: 2, -3, 4, -5, 6, -7, ...
/// </summary>
class PrintSequence
{
    /// <summary>
    /// Defines the entry point of the application.
    /// </summary>
    static void Main()
    {
        Console.WriteLine("2\r\n-3\r\n4\r\n-5\r\n6\r\n-7\r\n8\r\n-9\r\n10\r\n-11");
    }
}

