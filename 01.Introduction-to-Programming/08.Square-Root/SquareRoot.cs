using System;

/// <summary>
/// Write a program that calculates the square root of the number 12345 and prints it on the console.
/// </summary>
class SquareRoot
{
    /// <summary>
    /// Defines the entry point of the application.
    /// </summary>
    static void Main()
    {
        Console.WriteLine(Math.Sqrt(12345));
    }
}

